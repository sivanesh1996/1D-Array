class Pattern8
{
    public static void main(String[] args)
    {
     int row=1;
   
     while(row<=4) //statement la condition
        {
           int col=1;
         while(col<=4)
            {
             if(row+col==5)
                {
                 System.out.print("7"); //statement
                 }  
            else
                {
                System.out.print("5");        
                } 
              col++; 
             }
            System.out.println(); 
            row++;
            }
          
        }
}

