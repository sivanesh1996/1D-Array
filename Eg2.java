class Eg2
{
    public static void main(String[] args)
      {
        int Age[]={10,12,14};       // {index 0 ,index 1,index 2}
        System.out.println(Age[2]); // Age[index]
        System.out.println("The size of this array Age is:"+Age.length); //length variable in array returns the size of array not index
        System.out.println("The index of this array Age is:"+(Age.length-1));  // length-1 is index
     }
}
